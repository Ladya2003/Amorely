function buildGamesPatch(locale) {
  return {
    ...sharedTop(locale),
    common: sharedCommon(locale),
    ...sharedMeta(locale),
    ...gameCatalog(locale),
  };
}

for (const locale of LOCALES) {
  const filePath = path.join(localesDir, `${locale}.json`);
  const data = JSON.parse(fs.readFileSync(filePath, 'utf8'));
  const existingGames = data.games || {};
  const patch = buildGamesPatch(locale);

  data.games = deepMerge(existingGames, patch);

  for (const gameId of GAME_IDS) {
    if (existingGames[gameId]?.name) {
      data.games[gameId].name = existingGames[gameId].name;
    }
    if (existingGames[gameId]?.description) {
      data.games[gameId].description = existingGames[gameId].description;
    }
  }

  fs.writeFileSync(filePath, `${JSON.stringify(data, null, 2)}\n`, 'utf8');
  console.log(`Patched ${locale}.json`);
}
