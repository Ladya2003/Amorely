const formatMapLines = (entries: Record<string, string>) =>
  Object.entries(entries)
    .map(([ru, en]) => `  '${escapeTsString(ru)}': { en: '${escapeTsString(en)}' },`)
    .join('\n');

const main = () => {
  const locationEntries = GEO_LOCATIONS.map((loc) => {
    const nameEn = getLocationNameEn(loc.id, loc.city, loc.country);
    const continentEn = getContinentEn(loc.continent);
    const countryEn = getCountryEn(loc.country);
    const cityEn = getCityEn(loc.city);
    return `  '${escapeTsString(loc.id)}': { en: { name: '${escapeTsString(nameEn)}', continent: '${escapeTsString(continentEn)}', country: '${escapeTsString(countryEn)}', city: '${escapeTsString(cityEn)}' } },`;
  }).join('\n');

  const out = `/** Auto-generated by scripts/fetch-geo-i18n.ts — do not edit manually. */
import type { GameContentLocale } from '../locales';

export interface GeoLocationLocaleEntry {
  name: string;
  continent: string;
  country: string;
  city: string;
}

export const GEO_CONTINENT_I18N: Record<string, Partial<Record<GameContentLocale, string>>> = {
${formatMapLines(CONTINENT_EN)}
};

export const GEO_COUNTRY_I18N: Record<string, Partial<Record<GameContentLocale, string>>> = {
${formatMapLines(COUNTRY_EN)}
};

export const GEO_CITY_I18N: Record<string, Partial<Record<GameContentLocale, string>>> = {
${formatMapLines(CITY_EN)}
};

export const GEO_LOCATION_I18N: Record<string, Partial<Record<GameContentLocale, GeoLocationLocaleEntry>>> = {
${locationEntries}
};
`;

  const outDir = path.join(__dirname, '../src/i18n/generated');
  fs.mkdirSync(outDir, { recursive: true });
  fs.writeFileSync(path.join(outDir, 'geoLocationsI18n.ts'), out);

  const missing = GEO_LOCATIONS.filter((loc) => !LOCATION_NAME_EN[loc.id]);
  console.log(`Generated ${GEO_LOCATIONS.length} location translations.`);
  console.log(`Fallback city/country names: ${missing.length}`);
};

main();
